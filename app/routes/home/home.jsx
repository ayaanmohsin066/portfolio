export const meta = () => {
  return baseMeta({
    title: 'Ayaan Mohsin | Mathematics & Finance',
    description: `Portfolio of ${config.name} — mathematics, finance, data, and building.`,
  });
};

export const Home = () => {
  const [visibleSections, setVisibleSections] = useState([]);
  const [scrollIndicatorHidden, setScrollIndicatorHidden] = useState(false);
  const intro = useRef();
  const projectOne = useRef();
  const projectTwo = useRef();
  const projectThree = useRef();
  const details = useRef();

  useEffect(() => {
    const sections = [intro, projectOne, projectTwo, projectThree, details];

    const sectionObserver = new IntersectionObserver(
      (entries, observer) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const section = entry.target;
            observer.unobserve(section);
            if (visibleSections.includes(section)) return;
            setVisibleSections(prevSections => [...prevSections, section]);
          }
        });
      },
      { rootMargin: '0px 0px -10% 0px', threshold: 0.1 }
    );

    const indicatorObserver = new IntersectionObserver(
      ([entry]) => {
        setScrollIndicatorHidden(!entry.isIntersecting);
      },
      { rootMargin: '-100% 0px 0px 0px' }
    );

    sections.forEach(section => {
      sectionObserver.observe(section.current);
    });

    indicatorObserver.observe(intro.current);

    return () => {
      sectionObserver.disconnect();
      indicatorObserver.disconnect();
    };
  }, [visibleSections]);

  return (
    <div className={styles.home}>
      <Intro
        id="intro"
        sectionRef={intro}
        scrollIndicatorHidden={scrollIndicatorHidden}
      />
      <ProjectSummary
        id="project-1"
        sectionRef={projectOne}
        visible={visibleSections.includes(projectOne.current)}
        index={1}
        title="Honours Mathematics at University of Waterloo"
        description="Currently pursuing a Bachelor of Mathematics at the University of Waterloo. Completed first-semester coursework including calculus, linear algebra foundations, introductory computer science, and economics."
        buttonText="Learn More"
        buttonLink="/#details"
        model={{
          type: 'laptop',
          alt: 'LinkedIn profile',
          textures: [
            {
              srcSet: '/static/linkedin-preview.jpg 1280w',
              placeholder: '/static/linkedin-preview.jpg',
            },
          ],
        }}
      />
      <ProjectSummary
        id="project-2"
        alternate
        sectionRef={projectTwo}
        visible={visibleSections.includes(projectTwo.current)}
        index={2}
        title="Coursera Certifications — University of Michigan & Yale"
        description="Completed multiple online courses through Coursera from the University of Michigan and Yale University, covering Python programming, data structures, web data access, and foundational computing skills."
        buttonText="View Certificates"
        buttonLink="https://linktr.ee/ayaanmohsin59"
        model={{
          type: 'phone',
          alt: 'Coursera certifications — My Skills',
          textures: [
            {
              getCanvas: () => createCourseraPhone1Canvas(374, 512),
            },
            {
              getCanvas: () => createCourseraPhone2Canvas(374, 512),
            },
          ],
        }}
      />
      <ProjectSummary
        id="project-3"
        sectionRef={projectThree}
        visible={visibleSections.includes(projectThree.current)}
        index={3}
        title="Cotton Care — Certified Internship"
        description="Completed a certified internship contributing to quality control and operations. Certificate awarded upon completion."
        buttonText="View Certificate"
        buttonLink="/#details"
        model={{
          type: 'laptop',
          alt: 'Cotton Care internship certificate',
          textures: [
            {
              srcSet: `${sliceTexture} 800w, ${sliceTextureLarge} 1920w`,
              placeholder: sliceTexturePlaceholder,
            },
          ],
        }}
      />
      <Profile
        sectionRef={details}
        visible={visibleSections.includes(details.current)}
        id="details"
      />
      <Footer />
    </div>
  );
};
